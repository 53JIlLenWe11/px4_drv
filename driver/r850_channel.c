int r850_channel_get_regs(u32 no, u8 regs[2][R850_NUM_REGS - 0x08])
{
	if (no >= 3 && no <= 12)
		no -= 3;
	else if (no >= 22 && no <= 102)
		no -= 12;
	else
		return -ENOSYS;

	memcpy(regs[0], &ch_regs[no * 2], R850_NUM_REGS - 0x08);
	memcpy(regs[1], &ch_regs[(no * 2) + 1], R850_NUM_REGS - 0x08);

	return 0;
}
